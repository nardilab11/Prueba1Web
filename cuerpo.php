        <header class="subtitulo">Yakuza Kiwami</header>
        <div class="txtdesc">Yakuza Kiwami, siendo el remake del primer Yakuza, sirve de secuela a Yakuza 0 y, aunque 
            corre en la misma "engine", trae algunas características nuevas a la serie. Muchas personas 
            critican a Yakuza Kiwami por ser un peor Yakuza 0 (además de la misma "engine", ahora solo 
            hay un protagonista jugable y no hay tantos minijuegos). Sin embargo, en lo personal me gustaron 
            algunos aspectos más que Yakuza 0, como el sistema de subir nivel, los personajes y los 
            minijuegos. En lo que si estoy de acuerdo es que el precio no debería ser tan alto, porque aun 
            con las mejoras que hablé, sigue siendo un juego más corto de Yakuza 0. En resumen Yakuza Kiwami 
            le hace justicia a Yakuza 0, pero le bajaría un poco el precio.</div>
        <div class="txtdesc"> [+] Puntuación: 9.5/10</div>
        <div class="txtdesc"> [+] Precio: Un poco caro</div>
        <div class="txtdesc"> [+] Requerimientos: Regular</div>
