        <header class="subtitulo">Yakuza Kiwami 2</header>
        <div class="txtdesc">Yakuza Kiwami 2 sigue el patrón de hacer remake del primero, siendo éste de Yakuza 2. Kiwami 2 
            se desarrolla en una "engine" diferentes a los dos títulos pasados, por lo que podemos observar 
            un rediseño de muchos aspectos: nuevo tipo de combate, nueva "engine" para las físicas, nuevos 
            minijuegos y rediseños de algunos viejos, etc. Debo admitir que algunos cambios no me gustaron al 
            principio, aunque me terminé acostumbrando. Sobre la historia, me pareció que ha sido la mejor 
            entre los tres; hay momentos muy fuertes y llenos de emoción, particularmente una de las relaciones 
            del protagonista. En resumen, cuando uno pasa y se acostumbra a los cambios hechos, Kiwami es una 
            leal e innovador entrada en la serie de juegos Yakuza.</div>
        <div class="txtdesc"> [+] Puntuación: 9.5/10</div>
